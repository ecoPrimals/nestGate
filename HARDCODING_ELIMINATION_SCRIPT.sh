#!/bin/bash
# Hardcoding Elimination Script for NestGate
# This script systematically migrates hardcoded values to configuration

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "$SCRIPT_DIR"

echo "🔧 NestGate Hardcoding Elimination Script"
echo "=========================================="
echo ""

# Colors for output
